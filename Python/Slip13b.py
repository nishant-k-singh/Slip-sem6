class rectangle:
    def __init__(self,l,w):
        self.l=l
        self.w=w
    def area(self):
        print("area of rectangle",self.l*self.w)
    def perimeter(self):
        print("area of perimeter",self.l+self.w)    
r=rectangle(5, 5)
r.area()
r.perimeter()
    
    
        

        
        